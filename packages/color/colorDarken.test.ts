import { expect, it } from 'vitest'
import { colorDarken } from './colorDarken'

it('colorDarken', () => {
  expect(colorDarken('#0', 5)).toMatchInlineSnapshot('""')
  expect(colorDarken('#00', 5)).toMatchInlineSnapshot('""')
  expect(colorDarken('#000', 5)).toMatchInlineSnapshot('"#000000"')
  expect(colorDarken('#0006', 5)).toMatchInlineSnapshot('"#00000005"')
  expect(colorDarken('#fffff', 5)).toMatchInlineSnapshot('""')
  expect(colorDarken('#25eaa8', 5)).toMatchInlineSnapshot('"#127554"')
  expect(colorDarken('#25eaa80', 5)).toMatchInlineSnapshot('""')
  expect(colorDarken('#ff008b59', 5)).toMatchInlineSnapshot('"#7f004559"')
  expect(colorDarken('#ff008b598', 5)).toMatchInlineSnapshot('""')
  expect(colorDarken([255, 255, 255], 5)).toMatchInlineSnapshot('"#7f7f7f"')
  expect(colorDarken([36, 235, 168, 0], 5)).toMatchInlineSnapshot('"#12755400"')
  expect(colorDarken([255, 0, 139, 0.35], 5)).toMatchInlineSnapshot('"#7f004559"')
  expect(colorDarken([0, 255, 36, 0.5], 5)).toMatchInlineSnapshot('"#007f127f"')
  expect(colorDarken([0, 0, 0, 0.78], 5)).toMatchInlineSnapshot('"#000000c6"')
  expect(colorDarken([217], 5)).toMatchInlineSnapshot('""')
  expect(colorDarken([217, 8], 5)).toMatchInlineSnapshot('""')
  expect(colorDarken([217, 8, 16], 5)).toMatchInlineSnapshot('"#6c0408"')
  expect(colorDarken([217, 8, 16, 1], 5)).toMatchInlineSnapshot('"#6c0408ff"')
  expect(colorDarken('#25eaa8', 0)).toMatchInlineSnapshot('"#25eaa8"')
  expect(colorDarken('#25eaa8', 1)).toMatchInlineSnapshot('"#21d297"')
  expect(colorDarken('#25eaa8', 2)).toMatchInlineSnapshot('"#1dbb86"')
  expect(colorDarken('#25eaa8', 3)).toMatchInlineSnapshot('"#19a375"')
  expect(colorDarken('#25eaa8', 4)).toMatchInlineSnapshot('"#168c64"')
  expect(colorDarken('#25eaa8', 5)).toMatchInlineSnapshot('"#127554"')
  expect(colorDarken('#25eaa8', 6)).toMatchInlineSnapshot('"#0e5d43"')
  expect(colorDarken('#25eaa8', 7)).toMatchInlineSnapshot('"#0b4632"')
  expect(colorDarken('#25eaa8', 8)).toMatchInlineSnapshot('"#072e21"')
  expect(colorDarken('#25eaa8', 9)).toMatchInlineSnapshot('"#031710"')
  expect(colorDarken('#25eaa8', 10)).toMatchInlineSnapshot('"#000000"')
  expect(colorDarken('#25eaa8', 11)).toMatchInlineSnapshot('""')
})
